using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace UnitsNetGen.Generator;

internal static class QuantityEmitter
{
    public static string Emit(QuantitySelection selection, ISet<string> selectedQuantityNames)
    {
        QuantityDefinition quantity = selection.Definition;
        string unitType = quantity.Name + "Unit";
        var writer = new StringBuilder();

        writer.AppendLine("// <auto-generated />");
        writer.AppendLine("#nullable enable");
        writer.AppendLine();
        writer.Append("namespace ").Append(quantity.TargetNamespace).AppendLine(";");
        writer.AppendLine();
        writer.Append("public enum ").Append(unitType).AppendLine();
        writer.AppendLine("{");
        for (int index = 0; index < selection.Units.Count; index++)
        {
            writer.Append("    ").Append(selection.Units[index].SingularName).Append(" = ").Append(index).AppendLine(",");
        }

        writer.AppendLine("}");
        writer.AppendLine();
        writer.Append("public readonly partial struct ").Append(quantity.Name)
            .Append(" : global::UnitsNetGen.IQuantity<").Append(unitType).Append(">, ")
            .Append("global::System.IEquatable<").Append(quantity.Name).Append(">, ")
            .Append("global::System.IComparable<").Append(quantity.Name).AppendLine(">");
        writer.AppendLine("{");
        writer.Append("    private static readonly QuantityMetadata Metadata = new QuantityMetadata();").AppendLine();
        writer.AppendLine("    private readonly double _value;");
        writer.Append("    private readonly ").Append(unitType).AppendLine(" _unit;");
        writer.AppendLine();
        writer.Append("    public ").Append(quantity.Name).Append("(double value, ").Append(unitType).AppendLine(" unit)");
        writer.AppendLine("    {");
        writer.AppendLine("        _value = value;");
        writer.AppendLine("        _unit = unit;");
        writer.AppendLine("        _ = global::UnitsNetGen.QuantityOperations.GetBaseValue(value, unit, Metadata);");
        writer.AppendLine("    }");
        writer.AppendLine();
        writer.AppendLine("    public double Value => _value;");
        writer.Append("    public ").Append(unitType).AppendLine(" Unit => _unit;");
        writer.Append("    public static ").Append(unitType).Append(" BaseUnit => ").Append(unitType).Append('.').Append(quantity.BaseUnit).AppendLine(";");
        writer.Append("    public static global::System.Collections.Generic.IReadOnlyList<global::UnitsNetGen.UnitInfo<")
            .Append(unitType).AppendLine(">> UnitInfos => Metadata.Units;");
        writer.AppendLine("    internal double BaseValue => global::UnitsNetGen.QuantityOperations.GetBaseValue(_value, _unit, Metadata);");
        writer.AppendLine();

        foreach (UnitDefinition unit in selection.Units)
        {
            writer.Append("    public static ").Append(quantity.Name).Append(" From").Append(unit.PluralName).Append("(double value) => new ")
                .Append(quantity.Name).Append("(value, ").Append(unitType).Append('.').Append(unit.SingularName).AppendLine(");");
            writer.Append("    public double ").Append(unit.PluralName).Append(" => As(").Append(unitType).Append('.').Append(unit.SingularName).AppendLine(");");
        }

        writer.AppendLine();
        writer.Append("    public double As(").Append(unitType).AppendLine(" unit) =>");
        writer.AppendLine("        global::UnitsNetGen.QuantityOperations.Convert(_value, _unit, unit, Metadata);");
        writer.AppendLine();
        writer.Append("    public ").Append(quantity.Name).Append(" ToUnit(").Append(unitType).AppendLine(" unit) => new(As(unit), unit);");
        writer.AppendLine();
        writer.Append("    public static ").Append(quantity.Name).AppendLine(" Parse(string text, global::System.IFormatProvider? formatProvider = null)");
        writer.AppendLine("    {");
        writer.AppendLine("        if (!global::UnitsNetGen.QuantityOperations.TryParse(text, formatProvider, Metadata, out double value, out var unit))");
        writer.AppendLine("        {");
        writer.Append("            throw new global::System.FormatException(\"Could not parse ").Append(Escape(quantity.Name)).AppendLine(".\");");
        writer.AppendLine("        }");
        writer.AppendLine();
        writer.Append("        return new ").Append(quantity.Name).AppendLine("(value, unit);");
        writer.AppendLine("    }");
        writer.AppendLine();
        writer.Append("    public static bool TryParse(string? text, out ").Append(quantity.Name).AppendLine(" quantity)");
        writer.AppendLine("    {");
        writer.AppendLine("        if (global::UnitsNetGen.QuantityOperations.TryParse(text, null, Metadata, out double value, out var unit))");
        writer.AppendLine("        {");
        writer.Append("            quantity = new ").Append(quantity.Name).AppendLine("(value, unit);");
        writer.AppendLine("            return true;");
        writer.AppendLine("        }");
        writer.AppendLine();
        writer.AppendLine("        quantity = default;");
        writer.AppendLine("        return false;");
        writer.AppendLine("    }");
        writer.AppendLine();
        writer.Append("    public int CompareTo(").Append(quantity.Name).AppendLine(" other) => BaseValue.CompareTo(other.BaseValue);");
        writer.Append("    public bool Equals(").Append(quantity.Name).AppendLine(" other) => BaseValue.Equals(other.BaseValue);");
        writer.Append("    public override bool Equals(object? obj) => obj is ").Append(quantity.Name).AppendLine(" other && Equals(other);");
        writer.AppendLine("    public override int GetHashCode() => BaseValue.GetHashCode();");
        writer.AppendLine("    public override string ToString() => ToString(null, null);");
        writer.AppendLine("    public string ToString(string? format, global::System.IFormatProvider? formatProvider) =>");
        writer.AppendLine("        global::UnitsNetGen.QuantityOperations.Format(_value, _unit, format, formatProvider, Metadata);");
        writer.AppendLine();
        writer.Append("    public static ").Append(quantity.Name).Append(" operator +(").Append(quantity.Name).Append(" left, ").Append(quantity.Name)
            .AppendLine(" right) => new(left.Value + right.As(left.Unit), left.Unit);");
        writer.Append("    public static ").Append(quantity.Name).Append(" operator -(").Append(quantity.Name).Append(" left, ").Append(quantity.Name)
            .AppendLine(" right) => new(left.Value - right.As(left.Unit), left.Unit);");
        writer.Append("    public static ").Append(quantity.Name).Append(" operator *(").Append(quantity.Name)
            .AppendLine(" quantity, double scalar) => new(quantity.Value * scalar, quantity.Unit);");
        writer.Append("    public static ").Append(quantity.Name).Append(" operator /(").Append(quantity.Name)
            .AppendLine(" quantity, double scalar) => new(quantity.Value / scalar, quantity.Unit);");
        writer.Append("    public static bool operator ==(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.Equals(right);");
        writer.Append("    public static bool operator !=(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => !left.Equals(right);");
        writer.Append("    public static bool operator <(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.CompareTo(right) < 0;");
        writer.Append("    public static bool operator >(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.CompareTo(right) > 0;");
        writer.Append("    public static bool operator <=(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.CompareTo(right) <= 0;");
        writer.Append("    public static bool operator >=(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.CompareTo(right) >= 0;");

        EmitRelationships(writer, quantity.Name, selectedQuantityNames);

        writer.AppendLine();
        writer.Append("    private sealed class QuantityMetadata : global::UnitsNetGen.IQuantityMetadata<").Append(unitType).AppendLine(">");
        writer.AppendLine("    {");
        writer.Append("        private static readonly global::UnitsNetGen.UnitInfo<").Append(unitType).AppendLine(">[] AllUnits =");
        writer.AppendLine("        {");
        foreach (UnitDefinition unit in selection.Units)
        {
            writer.Append("            new global::UnitsNetGen.UnitInfo<").Append(unitType).Append(">(")
                .Append(unitType).Append('.').Append(unit.SingularName).Append(", \"").Append(Escape(unit.SingularName))
                .Append("\", \"").Append(Escape(unit.PluralName)).Append('"');
            foreach (UnitLocalizationDefinition localization in unit.Localizations)
            {
                writer.Append(", new global::UnitsNetGen.UnitLocalization(\"").Append(Escape(localization.Culture)).Append('"');
                foreach (string abbreviation in localization.Abbreviations)
                {
                    writer.Append(", \"").Append(Escape(abbreviation)).Append('"');
                }

                writer.Append(')');
            }

            writer.AppendLine("),");
        }

        writer.AppendLine("        };");
        writer.AppendLine();
        writer.Append("        public string Name => \"").Append(Escape(quantity.Name)).AppendLine("\";");
        writer.Append("        public ").Append(unitType).Append(" BaseUnit => ").Append(unitType).Append('.').Append(quantity.BaseUnit).AppendLine(";");
        writer.Append("        public global::System.Collections.Generic.IReadOnlyList<global::UnitsNetGen.UnitInfo<")
            .Append(unitType).AppendLine(">> Units => AllUnits;");
        writer.AppendLine();
        writer.Append("        public double ToBase(double x, ").Append(unitType).AppendLine(" unit) => unit switch");
        writer.AppendLine("        {");
        foreach (UnitDefinition unit in selection.Units)
        {
            writer.Append("            ").Append(unitType).Append('.').Append(unit.SingularName).Append(" => ")
                .Append(unit.FromUnitToBaseExpression).AppendLine(",");
        }

        writer.AppendLine("            _ => throw new global::System.ArgumentOutOfRangeException(nameof(unit), unit, null),");
        writer.AppendLine("        };");
        writer.AppendLine();
        writer.Append("        public double FromBase(double x, ").Append(unitType).AppendLine(" unit) => unit switch");
        writer.AppendLine("        {");
        foreach (UnitDefinition unit in selection.Units)
        {
            writer.Append("            ").Append(unitType).Append('.').Append(unit.SingularName).Append(" => ")
                .Append(unit.FromBaseToUnitExpression).AppendLine(",");
        }

        writer.AppendLine("            _ => throw new global::System.ArgumentOutOfRangeException(nameof(unit), unit, null),");
        writer.AppendLine("        };");
        writer.AppendLine("    }");
        writer.AppendLine("}");
        return writer.ToString();
    }

    private static void EmitRelationships(StringBuilder writer, string quantityName, ISet<string> selected)
    {
        if (quantityName == "Length" && selected.Contains("Area"))
        {
            writer.AppendLine("    public static Area operator *(Length left, Length right) => new(left.BaseValue * right.BaseValue, Area.BaseUnit);");
        }

        if (quantityName == "Length" && selected.Contains("Duration") && selected.Contains("Speed"))
        {
            writer.AppendLine("    public static Speed operator /(Length length, Duration duration) => new(length.BaseValue / duration.BaseValue, Speed.BaseUnit);");
        }

        if (quantityName == "Speed" && selected.Contains("Duration") && selected.Contains("Acceleration"))
        {
            writer.AppendLine("    public static Acceleration operator /(Speed speed, Duration duration) => new(speed.BaseValue / duration.BaseValue, Acceleration.BaseUnit);");
        }

        if (quantityName == "Mass" && selected.Contains("Acceleration") && selected.Contains("Force"))
        {
            writer.AppendLine("    public static Force operator *(Mass mass, Acceleration acceleration) => new(mass.BaseValue * acceleration.BaseValue, Force.BaseUnit);");
            writer.AppendLine("    public static Force operator *(Acceleration acceleration, Mass mass) => mass * acceleration;");
        }

        if (quantityName == "Force" && selected.Contains("Area") && selected.Contains("Pressure"))
        {
            writer.AppendLine("    public static Pressure operator /(Force force, Area area) => new(force.BaseValue / area.BaseValue, Pressure.BaseUnit);");
        }

        if (quantityName == "Force" && selected.Contains("Length") && selected.Contains("Energy"))
        {
            writer.AppendLine("    public static Energy operator *(Force force, Length length) => new(force.BaseValue * length.BaseValue, Energy.BaseUnit);");
        }

        if (quantityName == "Energy" && selected.Contains("Duration") && selected.Contains("Power"))
        {
            writer.AppendLine("    public static Power operator /(Energy energy, Duration duration) => new(energy.BaseValue / duration.BaseValue, Power.BaseUnit);");
        }
    }

    private static string Escape(string value) => value.Replace("\\", "\\\\").Replace("\"", "\\\"");
}
