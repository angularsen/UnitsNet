using System;
using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Text;

namespace UnitsNetGen.Generator;

internal static class QuantityEmitter
{
    public static string Emit(
        QuantitySelection selection,
        IReadOnlyList<EmittedQuantityRelation> relationships,
        QuantitySelection? affineOffset)
    {
        QuantityDefinition quantity = selection.Definition;
        string unitTypeName = quantity.Name + "Unit";
        bool unitsNetCompatibility = quantity.TargetNamespace == "UnitsNet";
        string unitType = unitsNetCompatibility
            ? "global::UnitsNet.Units." + unitTypeName
            : unitTypeName;
        var writer = new StringBuilder();

        writer.AppendLine("// <auto-generated />");
        writer.AppendLine("#nullable enable");
        writer.AppendLine();
        if (unitsNetCompatibility)
        {
            writer.AppendLine("namespace UnitsNet.Units");
            writer.AppendLine("{");
        }
        else
        {
            writer.Append("namespace ").Append(quantity.TargetNamespace).AppendLine(";");
            writer.AppendLine();
        }

        writer.Append("public enum ").Append(unitTypeName).AppendLine();
        writer.AppendLine("{");
        IReadOnlyDictionary<string, int> definitionIndexes = quantity.Units
            .Select((unit, index) => new { unit.SingularName, Index = index })
            .ToDictionary(item => item.SingularName, item => item.Index, StringComparer.Ordinal);
        foreach (UnitDefinition unit in selection.Units)
        {
            int definitionIndex = definitionIndexes[unit.SingularName];
            int enumValue = BuiltInUnitEnumValues.TryGet(quantity.SemanticId, unit.SingularName, out int stableValue)
                ? stableValue
                : definitionIndex + 1;
            writer.Append("    ").Append(unit.SingularName).Append(" = ").Append(enumValue).AppendLine(",");
        }

        writer.AppendLine("}");
        writer.AppendLine();
        if (unitsNetCompatibility)
        {
            writer.AppendLine("}");
            writer.AppendLine();
            writer.AppendLine("namespace UnitsNet");
            writer.AppendLine("{");
        }

        writer.Append("public readonly partial struct ").Append(quantity.Name).Append(" : ");
        if (quantity.IsAffine)
        {
            writer.Append("global::UnitsNet.Core.IAffineQuantity<").Append(quantity.Name).Append(", ")
                .Append(unitType).Append(", ").Append(QuantityType(affineOffset!.Definition)).Append(">, ");
        }
        else
        {
            string capabilityInterface = quantity.IsLogarithmic
                ? "global::UnitsNet.Core.ILogarithmicQuantity<"
                : "global::UnitsNet.Core.ILinearQuantity<";
            writer.Append(capabilityInterface).Append(quantity.Name).Append(", ").Append(unitType).Append(">, ");
        }
        writer
            .Append("global::System.IEquatable<").Append(quantity.Name).Append(">, ")
            .Append("global::System.IComparable<").Append(quantity.Name).AppendLine(">");
        writer.AppendLine("#if NET8_0_OR_GREATER");
        writer.Append("    , global::System.IParsable<").Append(quantity.Name).AppendLine(">");
        writer.Append("    , global::System.Numerics.IComparisonOperators<").Append(quantity.Name).Append(", ").Append(quantity.Name).AppendLine(", bool>");
        writer.AppendLine("#endif");
        writer.AppendLine("{");
        writer.Append("    private static readonly QuantityMetadata Metadata = new QuantityMetadata();").AppendLine();
        writer.AppendLine("    private readonly double _value;");
        writer.Append("    private readonly ").Append(unitType).AppendLine(" _unit;");
        writer.AppendLine();
        writer.Append("    public ").Append(quantity.Name).Append("(double value, ").Append(unitType).AppendLine(" unit)");
        writer.AppendLine("    {");
        writer.AppendLine("        _value = value;");
        writer.AppendLine("        _unit = unit;");
        writer.AppendLine("        _ = global::UnitsNetGen.QuantityOperations.GetBaseValue(value, unit, Metadata);");
        writer.AppendLine("    }");
        writer.AppendLine();
        writer.Append("    public static ").Append(quantity.Name).Append(" From(double value, ")
            .Append(unitType).AppendLine(" unit) => new(value, unit);");
        writer.Append("    public static double Convert(double value, ").Append(unitType).Append(" fromUnit, ")
            .Append(unitType).AppendLine(" toUnit) =>");
        writer.AppendLine("        global::UnitsNetGen.QuantityOperations.Convert(value, fromUnit, toUnit, Metadata);");
        writer.AppendLine();
        writer.AppendLine("    public double Value => _value;");
        writer.Append("    public ").Append(unitType).Append(" Unit => _unit.Equals(default(").Append(unitType)
            .AppendLine(")) ? BaseUnit : _unit;");
        writer.Append("    public static global::UnitsNet.Core.QuantityId QuantityId { get; } = new global::UnitsNet.Core.QuantityId(\"")
            .Append(Escape(quantity.SemanticId)).AppendLine("\");");
        writer.Append("    public static ").Append(unitType).Append(" BaseUnit => ").Append(unitType).Append('.').Append(quantity.BaseUnit).AppendLine(";");
        writer.Append("    public static ").Append(quantity.Name).AppendLine(" Zero { get; } = new(0, BaseUnit);");
        if (quantity.IsLogarithmic)
        {
            writer.Append("    public static double LogarithmicScalingFactor => ")
                .Append((10 * quantity.LogarithmicScalingFactor).ToString("R", CultureInfo.InvariantCulture))
                .AppendLine(";");
        }

        writer.Append("    public static global::System.Collections.Generic.IReadOnlyList<global::UnitsNetGen.UnitInfo<")
            .Append(unitType).AppendLine(">> UnitInfos => Metadata.Units;");
        writer.AppendLine("    internal double BaseValue => global::UnitsNetGen.QuantityOperations.GetBaseValue(_value, Unit, Metadata);");
        writer.AppendLine();

        foreach (UnitDefinition unit in selection.Units)
        {
            writer.Append("    public static ").Append(quantity.Name).Append(" From").Append(unit.PluralName).Append("(double value) => new ")
                .Append(quantity.Name).Append("(value, ").Append(unitType).Append('.').Append(unit.SingularName).AppendLine(");");
            writer.Append("    public double ").Append(unit.PluralName).Append(" => As(").Append(unitType).Append('.').Append(unit.SingularName).AppendLine(");");
        }

        writer.AppendLine();
        writer.Append("    public double As(").Append(unitType).AppendLine(" unit) =>");
        writer.AppendLine("        Convert(_value, Unit, unit);");
        writer.AppendLine();
        writer.Append("    public ").Append(quantity.Name).Append(" ToUnit(").Append(unitType).AppendLine(" unit) => new(As(unit), unit);");
        writer.AppendLine();
        writer.Append("    public static ").Append(quantity.Name).AppendLine(" Parse(string text, global::System.IFormatProvider? formatProvider = null)");
        writer.AppendLine("    {");
        writer.AppendLine("        if (!global::UnitsNetGen.QuantityOperations.TryParse(text, formatProvider, Metadata, out double value, out var unit))");
        writer.AppendLine("        {");
        writer.Append("            throw new global::System.FormatException(\"Could not parse ").Append(Escape(quantity.Name)).AppendLine(".\");");
        writer.AppendLine("        }");
        writer.AppendLine();
        writer.Append("        return new ").Append(quantity.Name).AppendLine("(value, unit);");
        writer.AppendLine("    }");
        writer.AppendLine();
        writer.Append("    public static bool TryParse(string? text, out ").Append(quantity.Name).AppendLine(" quantity)");
        writer.AppendLine("    {");
        writer.AppendLine("        if (global::UnitsNetGen.QuantityOperations.TryParse(text, null, Metadata, out double value, out var unit))");
        writer.AppendLine("        {");
        writer.Append("            quantity = new ").Append(quantity.Name).AppendLine("(value, unit);");
        writer.AppendLine("            return true;");
        writer.AppendLine("        }");
        writer.AppendLine();
        writer.AppendLine("        quantity = default;");
        writer.AppendLine("        return false;");
        writer.AppendLine("    }");
        writer.AppendLine("#if NET8_0_OR_GREATER");
        writer.Append("    public static bool TryParse(string? text, global::System.IFormatProvider? formatProvider, out ").Append(quantity.Name).AppendLine(" quantity)");
        writer.AppendLine("    {");
        writer.AppendLine("        if (global::UnitsNetGen.QuantityOperations.TryParse(text, formatProvider, Metadata, out double value, out var unit))");
        writer.AppendLine("        {");
        writer.Append("            quantity = new ").Append(quantity.Name).AppendLine("(value, unit);");
        writer.AppendLine("            return true;");
        writer.AppendLine("        }");
        writer.AppendLine();
        writer.AppendLine("        quantity = default;");
        writer.AppendLine("        return false;");
        writer.AppendLine("    }");
        writer.AppendLine("#endif");
        writer.AppendLine();
        writer.Append("    public int CompareTo(").Append(quantity.Name).AppendLine(" other) => BaseValue.CompareTo(other.BaseValue);");
        writer.Append("    public bool Equals(").Append(quantity.Name).AppendLine(" other) => BaseValue.Equals(other.BaseValue);");
        writer.Append("    public override bool Equals(object? obj) => obj is ").Append(quantity.Name).AppendLine(" other && Equals(other);");
        writer.AppendLine("    public override int GetHashCode() => BaseValue.GetHashCode();");
        writer.AppendLine("    public override string ToString() => ToString(null, null);");
        writer.AppendLine("    public string ToString(string? format, global::System.IFormatProvider? formatProvider) =>");
        writer.AppendLine("        global::UnitsNetGen.QuantityOperations.Format(_value, Unit, format, formatProvider, Metadata);");
        writer.AppendLine();
        if (quantity.IsLogarithmic)
        {
            EmitLogarithmicOperators(writer, quantity.Name, quantity.LogarithmicScalingFactor);
        }
        else if (quantity.IsAffine)
        {
            EmitAffineOperators(writer, selection, affineOffset!);
        }
        else
        {
            writer.Append("    public static ").Append(quantity.Name).Append(" operator -(").Append(quantity.Name)
                .AppendLine(" quantity) => new(-quantity.Value, quantity.Unit);");
            writer.Append("    public static ").Append(quantity.Name).Append(" operator +(").Append(quantity.Name).Append(" left, ").Append(quantity.Name)
                .AppendLine(" right) => new(left.Value + right.As(left.Unit), left.Unit);");
            writer.Append("    public static ").Append(quantity.Name).Append(" operator -(").Append(quantity.Name).Append(" left, ").Append(quantity.Name)
                .AppendLine(" right) => new(left.Value - right.As(left.Unit), left.Unit);");
            writer.Append("    public static ").Append(quantity.Name).Append(" operator *(").Append(quantity.Name)
                .AppendLine(" quantity, double scalar) => new(quantity.Value * scalar, quantity.Unit);");
            writer.Append("    public static ").Append(quantity.Name).Append(" operator *(double scalar, ").Append(quantity.Name)
                .AppendLine(" quantity) => quantity * scalar;");
            writer.Append("    public static ").Append(quantity.Name).Append(" operator /(").Append(quantity.Name)
                .AppendLine(" quantity, double scalar) => new(quantity.Value / scalar, quantity.Unit);");
        }

        writer.Append("    public static bool operator ==(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.Equals(right);");
        writer.Append("    public static bool operator !=(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => !left.Equals(right);");
        writer.Append("    public static bool operator <(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.CompareTo(right) < 0;");
        writer.Append("    public static bool operator >(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.CompareTo(right) > 0;");
        writer.Append("    public static bool operator <=(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.CompareTo(right) <= 0;");
        writer.Append("    public static bool operator >=(").Append(quantity.Name).Append(" left, ").Append(quantity.Name).AppendLine(" right) => left.CompareTo(right) >= 0;");

        EmitRelationships(writer, relationships);

        writer.AppendLine();
        writer.Append("    private sealed class QuantityMetadata : global::UnitsNetGen.IQuantityMetadata<").Append(unitType).AppendLine(">");
        writer.AppendLine("    {");
        writer.Append("        private static readonly global::UnitsNetGen.UnitInfo<").Append(unitType).AppendLine(">[] AllUnits =");
        writer.AppendLine("        {");
        foreach (UnitDefinition unit in selection.Units)
        {
            writer.Append("            new global::UnitsNetGen.UnitInfo<").Append(unitType).Append(">(")
                .Append(unitType).Append('.').Append(unit.SingularName).Append(", \"").Append(Escape(unit.SingularName))
                .Append("\", \"").Append(Escape(unit.PluralName)).Append('"');
            foreach (UnitLocalizationDefinition localization in unit.Localizations)
            {
                writer.Append(", new global::UnitsNetGen.UnitLocalization(\"").Append(Escape(localization.Culture)).Append('"');
                foreach (string abbreviation in localization.Abbreviations)
                {
                    writer.Append(", \"").Append(Escape(abbreviation)).Append('"');
                }

                writer.Append(')');
            }

            writer.AppendLine("),");
        }

        writer.AppendLine("        };");
        writer.AppendLine();
        writer.Append("        public string Name => \"").Append(Escape(quantity.Name)).AppendLine("\";");
        writer.Append("        public ").Append(unitType).Append(" BaseUnit => ").Append(unitType).Append('.').Append(quantity.BaseUnit).AppendLine(";");
        writer.Append("        public global::System.Collections.Generic.IReadOnlyList<global::UnitsNetGen.UnitInfo<")
            .Append(unitType).AppendLine(">> Units => AllUnits;");
        writer.AppendLine();
        writer.Append("        public double ToBase(double x, ").Append(unitType).AppendLine(" unit) => unit switch");
        writer.AppendLine("        {");
        foreach (UnitDefinition unit in selection.Units)
        {
            writer.Append("            ").Append(unitType).Append('.').Append(unit.SingularName).Append(" => ")
                .Append(unit.FromUnitToBaseExpression).AppendLine(",");
        }

        writer.AppendLine("            _ => throw new global::System.ArgumentOutOfRangeException(nameof(unit), unit, null),");
        writer.AppendLine("        };");
        writer.AppendLine();
        writer.Append("        public double FromBase(double x, ").Append(unitType).AppendLine(" unit) => unit switch");
        writer.AppendLine("        {");
        foreach (UnitDefinition unit in selection.Units)
        {
            writer.Append("            ").Append(unitType).Append('.').Append(unit.SingularName).Append(" => ")
                .Append(unit.FromBaseToUnitExpression).AppendLine(",");
        }

        writer.AppendLine("            _ => throw new global::System.ArgumentOutOfRangeException(nameof(unit), unit, null),");
        writer.AppendLine("        };");
        writer.AppendLine("    }");
        writer.AppendLine("}");
        if (unitsNetCompatibility)
        {
            writer.AppendLine("}");
        }

        return writer.ToString();
    }

    private static void EmitRelationships(
        StringBuilder writer,
        IReadOnlyList<EmittedQuantityRelation> relationships)
    {
        foreach (EmittedQuantityRelation relationship in relationships)
        {
            string leftType = QuantityType(relationship.Left.Definition);
            string rightType = QuantityType(relationship.Right.Definition);
            string leftValue = ConversionExpression.Substitute(
                relationship.LeftUnit.FromBaseToUnitExpression,
                relationship.Operator == "inverse" ? "BaseValue" : "left.BaseValue");
            string rightValue = ConversionExpression.Substitute(
                relationship.RightUnit.FromBaseToUnitExpression,
                "right.BaseValue");
            if (relationship.Operator == "inverse")
            {
                string inverseValue = "1 / (" + leftValue + ")";
                writer.Append("    public ").Append(rightType).Append(" Inverse() => ")
                    .Append(ConstructFromRelationUnit(
                        relationship.Right,
                        relationship.RightUnit,
                        inverseValue))
                    .AppendLine(";");
                continue;
            }

            QuantitySelection result = relationship.Result!;
            string relationValue = "(" + leftValue + ") " + relationship.Operator + " (" + rightValue + ")";
            writer.Append("    public static ").Append(QuantityType(result.Definition)).Append(" operator ")
                .Append(relationship.Operator).Append('(').Append(leftType).Append(" left, ")
                .Append(rightType).Append(" right) => ")
                .Append(ConstructFromRelationUnit(result, relationship.ResultUnit!, relationValue))
                .AppendLine(";");
        }
    }

    private static string ConstructFromRelationUnit(
        QuantitySelection result,
        UnitDefinition relationUnit,
        string value)
    {
        string baseValue = ConversionExpression.Substitute(relationUnit.FromUnitToBaseExpression, value);
        return "new " + QuantityType(result.Definition) + "(" + baseValue + ", " +
               UnitType(result.Definition) + "." + result.Definition.BaseUnit + ")";
    }

    private static string QuantityType(QuantityDefinition definition) =>
        "global::" + definition.TargetNamespace + "." + definition.Name;

    private static string UnitType(QuantityDefinition definition) =>
        definition.TargetNamespace == "UnitsNet"
            ? "global::UnitsNet.Units." + definition.Name + "Unit"
            : "global::" + definition.TargetNamespace + "." + definition.Name + "Unit";

    private static void EmitLogarithmicOperators(
        StringBuilder writer,
        string quantityName,
        double logarithmicScalingFactor)
    {
        string scalingFactor = (10 * logarithmicScalingFactor).ToString("R", CultureInfo.InvariantCulture);
        writer.Append("    public static ").Append(quantityName).Append(" operator -(").Append(quantityName)
            .AppendLine(" quantity) => new(-quantity.Value, quantity.Unit);");
        writer.Append("    public static ").Append(quantityName).Append(" operator +(").Append(quantityName).Append(" left, ")
            .Append(quantityName).Append(" right) => new(").Append(scalingFactor)
            .Append(" * global::System.Math.Log10(global::System.Math.Pow(10, left.Value / ")
            .Append(scalingFactor)
            .Append(") + global::System.Math.Pow(10, right.As(left.Unit) / ")
            .Append(scalingFactor).AppendLine(")), left.Unit);");
        writer.Append("    public static ").Append(quantityName).Append(" operator -(").Append(quantityName).Append(" left, ")
            .Append(quantityName).Append(" right) => new(").Append(scalingFactor)
            .Append(" * global::System.Math.Log10(global::System.Math.Pow(10, left.Value / ")
            .Append(scalingFactor)
            .Append(") - global::System.Math.Pow(10, right.As(left.Unit) / ")
            .Append(scalingFactor).AppendLine(")), left.Unit);");
        writer.Append("    public static ").Append(quantityName).Append(" operator *(").Append(quantityName)
            .AppendLine(" quantity, double scalar) => new(quantity.Value + scalar, quantity.Unit);");
        writer.Append("    public static ").Append(quantityName).Append(" operator *(").Append("double scalar, ")
            .Append(quantityName).AppendLine(" quantity) => quantity * scalar;");
        writer.Append("    public static ").Append(quantityName).Append(" operator /(").Append(quantityName)
            .AppendLine(" quantity, double scalar) => new(quantity.Value - scalar, quantity.Unit);");
        writer.Append("    public static double operator /(").Append(quantityName).Append(" left, ").Append(quantityName)
            .AppendLine(" right) => left.Value - right.As(left.Unit);");
    }

    private static void EmitAffineOperators(
        StringBuilder writer,
        QuantitySelection quantity,
        QuantitySelection offset)
    {
        string quantityType = quantity.Definition.Name;
        string offsetType = QuantityType(offset.Definition);
        string offsetBaseUnit = UnitType(offset.Definition) + "." + offset.Definition.BaseUnit;
        writer.Append("    public static ").Append(quantityType).Append(" operator +(").Append(quantityType)
            .Append(" left, ").Append(offsetType).AppendLine(" right) =>")
            .Append("        new(left.BaseValue + right.BaseValue, BaseUnit);").AppendLine();
        writer.Append("    public static ").Append(quantityType).Append(" operator +(").Append(offsetType)
            .Append(" left, ").Append(quantityType).AppendLine(" right) => right + left;");
        writer.Append("    public static ").Append(quantityType).Append(" operator -(").Append(quantityType)
            .Append(" left, ").Append(offsetType).AppendLine(" right) =>")
            .Append("        new(left.BaseValue - right.BaseValue, BaseUnit);").AppendLine();
        writer.Append("    public static ").Append(offsetType).Append(" operator -(").Append(quantityType)
            .Append(" left, ").Append(quantityType).AppendLine(" right) =>")
            .Append("        new(left.BaseValue - right.BaseValue, ").Append(offsetBaseUnit).AppendLine(");");
    }

    private static string Escape(string value) => value.Replace("\\", "\\\\").Replace("\"", "\\\"");
}
